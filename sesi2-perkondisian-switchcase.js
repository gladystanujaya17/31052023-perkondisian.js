// CONTOH 1

//switch case jangan lupa pakai break pada setiap case dan default
var pesanan = 'es teh manis'

switch(pesanan) {
    case 'air mineral':
        console.log('Anda pesan kopi')
        break;
    case 'es teh manis':
        console.log('Anda pesan es teh manis')
        break;
    case 'kopi':
        console.log('Anda pesan kopi')
        break;
    default:
        console.log('Minuman tidak ada di menu')
        break;
}

